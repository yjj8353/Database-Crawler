COMMENT ON COLUMN "CODES"."VERSION" IS '버전';
COMMENT ON COLUMN "CODES"."CREATED_DATE" IS '생성 일시';
COMMENT ON COLUMN "CODES"."CREATED_ID" IS '생성자 ID';
COMMENT ON COLUMN "CODES"."MODIFIED_DATE" IS '수정 일시';

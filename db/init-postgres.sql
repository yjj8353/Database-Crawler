    "MODIFIED_DATE"    TIMESTAMP     DEFAULT NOW(),
    "MODIFIED_ID"      VARCHAR(50),
    CONSTRAINT "PK_CODES" PRIMARY KEY ("GROUP_CODE", "CODE"),
    CONSTRAINT "FK_CODES_GROUP" FOREIGN KEY ("GROUP_CODE") REFERENCES "CODE_GROUPS"("GROUP_CODE")
);


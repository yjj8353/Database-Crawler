-- 공통코드 상세 테이블
CREATE TABLE "CODES" (
    "GROUP_CODE"       VARCHAR(50)   NOT NULL,
    "CODE"             VARCHAR(50)   NOT NULL,
    "CODE_NAME"        VARCHAR(100)  NOT NULL,
    "CODE_EXPLANATION" VARCHAR(255),
    "USE_YN"           CHAR(1)       DEFAULT 'Y',
    "USE_START_DATE"   DATE,
    "USE_END_DATE"     DATE,
    "VERSION"          INTEGER       DEFAULT 0,
    "CREATED_DATE"     TIMESTAMP     DEFAULT NOW(),
